public class DevUse {
    public static void main(String[] args) {
        System.out.println(10/3);
        System.out.println(10.0/3); // 3.3333333335
        double d = 10/3; // 转成int 3 然后再转成进度大double 3.0
        System.out.println(d);
        System.out.println("------------------");
        System.out.println(10%3);
        System.out.println(-10%3);

    }
}
